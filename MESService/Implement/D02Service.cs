        private async Task<List<SuperResultTranfer>> GET_PO_List()
        {
            List<SuperResultTranfer> result = new List<SuperResultTranfer>();
            string sql = @"SELECT tdpdotpl.PO_CODE, tdpdotpl.CREATE_DTM   FROM tdpdotpl
                            GROUP BY tdpdotpl.PO_CODE
                            ORDER BY  tdpdotpl.PDOTPL_IDX DESC
                            LIMIT 20";
            DataSet ds = await MySQLHelper.FillDataSetBySQLAsync(GlobalHelper.MariaDBConectionString, sql);
            for (int i = 0; i < ds.Tables.Count; i++)
            {
                DataTable dt = ds.Tables[i];
                result.AddRange(SQLHelper.ToList<SuperResultTranfer>(dt));
            }
            return result;
        }

