        [HttpPost]
        public async Task<BaseResult> DGV_LOAD()
        {
            BaseResult BaseResult = new BaseResult();
            try
            {
                System.Diagnostics.Debug.WriteLine("C19Controller - DGV_LOAD called");
                BaseParameter BaseParameter = JsonConvert.DeserializeObject<BaseParameter>(Request.Form["BaseParameter"]);
                System.Diagnostics.Debug.WriteLine($"C19Controller - Parameter: {JsonConvert.SerializeObject(BaseParameter)}");

                BaseResult = await _C19Service.DGV_LOAD(BaseParameter);

                System.Diagnostics.Debug.WriteLine($"C19Controller - Result Success: {BaseResult.Success}");
                if (BaseResult.DataGridView7 != null)
                {
                    System.Diagnostics.Debug.WriteLine($"C19Controller - Result DataGridView7 Count: {BaseResult.DataGridView7.Count}");
                }
                else
                {
                    System.Diagnostics.Debug.WriteLine("C19Controller - Result DataGridView7 is null");
                }
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine($"C19Controller - DGV_LOAD Error: {ex.Message}");
                BaseResult.Error = ex.Message;
            }
            return BaseResult;
        }
