        // Hàm kiểm tra trạng thái xuất Excel
        [HttpPost]
        public async Task<BaseResult> CheckExportStatus(string jobId)
        {
            BaseResult result = new BaseResult();
            try
            {
                var jobInfo = CacheHelper.Get<ExportJobInfo>(jobId);
                if (jobInfo == null)
                {
                    result.Error = "Job không tồn tại hoặc đã hết hạn";
                    return result;
                }

                result.Code = jobInfo.Status;
                result.Message = jobInfo.GetDetailMessage(); // Dùng message chi tiết
                result.Progress = jobInfo.Progress;
                result.LastUpdated = jobInfo.LastUpdated;
                result.JobId = jobId;

                // Thêm thông tin chi tiết
                result.CurrentRow = jobInfo.CurrentRow;
                result.TotalRows = jobInfo.TotalRows;
                result.CurrentAction = jobInfo.CurrentAction;

                if (jobInfo.Status == "Completed")
                {
                    result.DownloadUrl = jobInfo.DownloadUrl;
                    result.Message = $"Xuất file thành công! ({jobInfo.TotalRows} dòng)";
                }
                else if (jobInfo.Status == "Failed")
                {
                    result.Error = jobInfo.Error ?? "Xuất file thất bại";
                }
            }
            catch (Exception ex)
            {
                result.Error = ex.Message;
            }
            return result;
        }
