        private readonly IZ05Service _Z05Service;
        public Z05Controller(IZ05Service Z05Service)
        {
            _Z05Service = Z05Service;
        }
        public IActionResult Index()
        {
            return View();
        }
        [HttpPost]
        public async Task<JsonResult> Load([FromForm] string BaseParameter)
        {
            try
            {
                BaseResult result = await _Z05Service.Load();
                return Json(result);
            }
            catch (Exception ex)
            {
                BaseResult result = new BaseResult
                {
                    Success = false,
                    Error = ex.Message
                };
                return Json(result);
            }
        }

        [HttpPost]
        public async Task<BaseResult> Buttonfind_Click()
        {
            BaseResult BaseResult = new BaseResult();
            try
            {
                BaseParameter BaseParameter = JsonConvert.DeserializeObject<BaseParameter>(Request.Form["BaseParameter"]);
                BaseResult = await _Z05Service.Buttonfind_Click(BaseParameter);
            }
            catch (Exception ex)
            {
                BaseResult.Error = ex.Message;
            }
            return BaseResult;
        }
