        [HttpPost]
        public async Task<BaseResult> COMLIST_LINE()
        {
            BaseResult BaseResult = new BaseResult();
            try
            {
                BaseParameter BaseParameter = JsonConvert.DeserializeObject<BaseParameter>(Request.Form["BaseParameter"]);
                BaseResult = await _B09Service.COMLIST_LINE(BaseParameter);

                if (BaseResult != null)
                {
                    if (BaseResult.cb_Stage1 != null)
                    {
                        string Language = _cookieHelper.GetCookie(GlobalHelper.Language) ?? "EN";

                        for (int i = 0; i < BaseResult.cb_Stage1.Count; i++)
                        {
                            BaseResult.cb_Stage1[i].CD_SYS_NOTE = BaseResult.cb_Stage1[i].CD_NM_EN;
                            if (Language == GlobalHelper.LanguageCodeKR)
                            {
                                BaseResult.cb_Stage1[i].CD_SYS_NOTE = BaseResult.cb_Stage1[i].CD_NM_HAN;
                            }
                        }
                        BaseResult.T2_S1 = BaseResult.cb_Stage1;
                    }
                }
            }
            catch (Exception ex)
            {
                BaseResult.Error = ex.Message;
            }
            return BaseResult;
        }
