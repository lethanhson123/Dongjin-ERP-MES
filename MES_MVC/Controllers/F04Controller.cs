        public IActionResult PrintQRCode(string codes)
        {
            if (string.IsNullOrEmpty(codes))
            {
                return RedirectToAction("Index");
            }

            var errorCodesList = codes.Split(',').ToList();
            var ngLists = _F04Service.GetNGListsByErrorCodes(errorCodesList);

            return View(ngLists);
        }
