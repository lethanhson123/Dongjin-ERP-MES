        [HttpPost]
        public async Task<JsonResult> GetActiveShifts()
        {
            var result = await _A12Service.GetAllActiveShifts();
            return Json(new
            {
                success = result.Success,
                data = result.ShiftTimes,
                error = result.Error
            });
